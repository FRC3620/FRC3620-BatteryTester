package org.usfirst.frc3620;

public class BatteryInfo {
    String id;
    String makeModel;
    int year;
    double nominalCapacity;

    public String getId() {
        return id;
    }

    public String getMakeModel() {
        return makeModel;
    }

    public double getNominalCapacity() {
        return nominalCapacity;
    }

    public int getYear() {
        return year;
    }
}
